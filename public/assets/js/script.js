function applyI18n(lang, options = {}){
  lang = lang === 'en' ? 'en' : 'bn';
  const dict = i18n[lang];
  document.documentElement.lang = (lang === 'en' ? 'en' : 'bn');
  document.documentElement.setAttribute('data-lang', lang);
  if (options.persist) {
    try { localStorage.setItem('mw-lang-choice', lang); } catch (_) {}
  }
  if (langBnBtn && langEnBtn) {
    langBnBtn.setAttribute('aria-pressed', String(lang === 'bn'));
    langEnBtn.setAttribute('aria-pressed', String(lang === 'en'));
  }
  for(const el of document.querySelectorAll('[data-i18n]')){
    const key = el.getAttribute('data-i18n');
    if(dict[key] !== undefined){ el.textContent = dict[key]; }
  }
  const nameEl = document.getElementById('name');
  const phoneEl = document.getElementById('phone');
  const addressEl = document.getElementById('address');
  if (nameEl) nameEl.placeholder = (lang==='en'?'Full name':'পূর্ণ নাম');
  if (phoneEl) phoneEl.placeholder = '01XXXXXXXXX';
  if (addressEl) addressEl.placeholder = (lang==='en'?'House/Road/Thana or pick from map':'বাড়ি/রোড/থানার নাম লিখুন বা মানচিত্র থেকে নির্বাচন করুন');
  document.querySelectorAll('.toggle-btn').forEach(btn=>{
    if(btn.getAttribute('aria-expanded')==='true'){
      btn.textContent = (lang==='en'?'Show less':'সংক্ষিপ্ত করুন');
    }else{
      const isMethod = btn.hasAttribute('data-i18n') && btn.getAttribute('data-i18n') === 'btn_method';
      btn.textContent = isMethod ? dict.btn_method : dict.btn_more;
    }
  });
  document.dispatchEvent(new CustomEvent('mw:languagechange', { detail: { lang } }));
}

// ===== Intersection Observer for fade-in =====
const toObserve = document.querySelectorAll('[data-observe]');
const io = ('IntersectionObserver' in window) ? new IntersectionObserver((entries)=>{
  entries.forEach(e=>{ if(e.isIntersecting) e.target.classList.add('is-visible'); });
}, { threshold: .15 }) : null;
toObserve.forEach(el=> io && io.observe(el));

// ===== Delegated toggle handler with ARIA =====
document.addEventListener('click', (e) => {
  const btn = e.target.closest('[data-toggle]');
  if (!btn) return;
  const sel = btn.getAttribute('data-toggle');
  const panel = document.querySelector(sel);
  if (!panel) return;
  const willOpen = panel.hasAttribute('hidden');
  if (willOpen) {
    panel.removeAttribute('hidden');
    panel.style.display = 'block';
  } else {
    panel.setAttribute('hidden', '');
    panel.style.display = 'none';
  }
  const lang = document.documentElement.getAttribute('data-lang')||'bn';
  const isMethod = btn.hasAttribute('data-i18n') && btn.getAttribute('data-i18n') === 'btn_method';
  btn.textContent = willOpen
    ? (lang==='en'?'Show less':'সংক্ষিপ্ত করুন')
    : (isMethod ? i18n[lang].btn_method : i18n[lang].btn_more);
  btn.setAttribute('aria-expanded', String(willOpen));
  panel.setAttribute('aria-hidden', String(!willOpen));
});

